package com.chello.core.logging.api;

/**
 * The Enum for different log level.
 * 
 * @author Cognizant
 * @version 1.0
 * @since 2022-11-01
 */
public enum ChelloLogLevel {

	/** The debug log. */
	DEBUG,
	/** The info log. */
	INFO,
	/** The warn log. */
	WARN,
	/** The error log. */
	ERROR,
	/** The trace log. */
	TRACE;

}
